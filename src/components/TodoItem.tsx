import { useEffect, useRef } from 'react'
import { todoItem } from '../lib/types'
import { toggleEdit, toggleToDo, updateToDo } from '../lib/features/todo/todoSlice';
import { useAppDispatch } from '../hooks';

interface TodoItemProps {
    item: todoItem,
    onItemRemove(id: string): void,
}

const TodoItem = ({ item: { id, name, editing, completed }, onItemRemove }: TodoItemProps) => {
    const inputText = useRef<HTMLInputElement>(null);
    const dispatch = useAppDispatch();

    useEffect(() => inputText.current?.focus());

    const editItem = (id: string) => {
        const itemText = inputText?.current?.value.trim() || "";
        if (itemText !== "") {
            dispatch(updateToDo({ id, name: itemText }));
            dispatch(toggleEdit({ id }));
        }

    }
    console.log(completed, "completed")
    return (
        <div className='flex items-center justify-between'>
            {
                editing ? <>
                    <input defaultValue={name} ref={inputText} />
                    <span className='cursor-pointer' onClick={() => { editItem(id) }}>
                        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-6 h-6">
                            <path d="M5.625 1.5c-1.036 0-1.875.84-1.875 1.875v17.25c0 1.035.84 1.875 1.875 1.875h12.75c1.035 0 1.875-.84 1.875-1.875V12.75A3.75 3.75 0 0 0 16.5 9h-1.875a1.875 1.875 0 0 1-1.875-1.875V5.25A3.75 3.75 0 0 0 9 1.5H5.625Z" />
                            <path d="M12.971 1.816A5.23 5.23 0 0 1 14.25 5.25v1.875c0 .207.168.375.375.375H16.5a5.23 5.23 0 0 1 3.434 1.279 9.768 9.768 0 0 0-6.963-6.963Z" />
                        </svg>
                    </span>
                    <span onClick={() => dispatch(toggleEdit({ id }))} className='cursor-pointer'>
                        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-6 h-6">
                            <path fillRule="evenodd" d="M12 2.25c-5.385 0-9.75 4.365-9.75 9.75s4.365 9.75 9.75 9.75 9.75-4.365 9.75-9.75S17.385 2.25 12 2.25Zm-1.72 6.97a.75.75 0 1 0-1.06 1.06L10.94 12l-1.72 1.72a.75.75 0 1 0 1.06 1.06L12 13.06l1.72 1.72a.75.75 0 1 0 1.06-1.06L13.06 12l1.72-1.72a.75.75 0 1 0-1.06-1.06L12 10.94l-1.72-1.72Z" clipRule="evenodd" />
                        </svg>
                    </span>
                </> :
                    <>
                        <>
                            <input type='checkbox' onClick={() => dispatch(toggleToDo({ id }))} />
                            <span className={completed ? 'line-through' : ''}>{name}</span>
                            <div className='flex items-center justify-between'>
                                <span className='cursor-pointer' onClick={() => dispatch(toggleEdit({ id }))}>
                                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor" className="w-6 h-6">
                                        <path stroke-linecap="round" strokeLinejoin="round" d="m16.862 4.487 1.687-1.688a1.875 1.875 0 1 1 2.652 2.652L6.832 19.82a4.5 4.5 0 0 1-1.897 1.13l-2.685.8.8-2.685a4.5 4.5 0 0 1 1.13-1.897L16.863 4.487Zm0 0L19.5 7.125" />
                                    </svg>
                                </span>
                                <span className='cursor-pointer' role="button" onClick={() => onItemRemove(id)}>
                                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor" className="w-6 h-6">
                                        <path stroke-linecap="round" strokeLinejoin="round" d="m14.74 9-.346 9m-4.788 0L9.26 9m9.968-3.21c.342.052.682.107 1.022.166m-1.022-.165L18.16 19.673a2.25 2.25 0 0 1-2.244 2.077H8.084a2.25 2.25 0 0 1-2.244-2.077L4.772 5.79m14.456 0a48.108 48.108 0 0 0-3.478-.397m-12 .562c.34-.059.68-.114 1.022-.165m0 0a48.11 48.11 0 0 1 3.478-.397m7.5 0v-.916c0-1.18-.91-2.164-2.09-2.201a51.964 51.964 0 0 0-3.32 0c-1.18.037-2.09 1.022-2.09 2.201v.916m7.5 0a48.667 48.667 0 0 0-7.5 0" />
                                    </svg>
                                </span>
                            </div>
                        </>
                    </>
            }
        </div >
    )
}


export default TodoItem